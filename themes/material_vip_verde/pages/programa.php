							<p>El programa inaugural de IMMATERIAL presenta una selección de piezas nuevas y recién creadas por un grupo de artistas internacionales que trabajan en coreografía y performance.</p>
							<p>Durante los cuatro días que dura el programa las obras se presentarán tanto en los salones de la feria como en el vestíbulo de Expo Reforma, a veces sin previo aviso. La serie intenta darle un espacio al arte vivo y crear una plataforma para la presentación de una singular selección de trabajos, realizada por primera vez en el marco de una feria de arte en Latinoamérica.</p>
							<p>Las obras atraviesan distintas estrategias performáticas, se dirigen abiertamente al espectador y cuestionan los contextos social y arquitectónico de su presentación. Aspiran a una renegociación de las convenciones típicas del espectador y harán destacar el estado actual del objeto inmaterial y la necesidad de un modelo alternativo con respecto al mismo.  </p>
							<p>Algunos trabajos serán coproducidos con el público y otros intentarán difuminar las fronteras de la interacción al exponer su propio protocolo de representación o al ejecutar bajo el entendido de que la coreografía utiliza la danza como herramienta para organizar el tiempo. Fundamentalmente, el programa hablará de las relaciones que se activan en los contextos del intercambio y la economía de la experiencia en las que se están presentando.</p>
							<p>Alineados con la premisa original de la feria, como espacio de búsqueda e investigación artísticas, IMMATERIAL propone una manera de repensar los mecanismos del mercado del arte.</p>
							<p>Programador de Performance: <br>
							Michelangelo Miccolis</p>
